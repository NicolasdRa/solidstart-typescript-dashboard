// Image optimization utilities

/**
 * Generate optimized image URLs for different sizes
 * Works with image CDNs that support URL parameters
 */
export function generateImageSrcSet(
  baseUrl: string,
  sizes: number[] = [200, 400, 800, 1200]
): string {
  // Check if URL already has query parameters
  const separator = baseUrl.includes('?') ? '&' : '?'
  
  return sizes
    .map(size => `${baseUrl}${separator}w=${size} ${size}w`)
    .join(', ')
}

/**
 * Generate a low-quality image placeholder (LQIP)
 * In production, you'd generate this server-side
 */
export function generateBlurDataURL(width: number = 40, height: number = 30): string {
  // Simple SVG placeholder
  return `data:image/svg+xml,%3Csvg width='${width}' height='${height}' xmlns='http://www.w3.org/2000/svg'%3E%3Cfilter id='b'%3E%3CfeGaussianBlur stdDeviation='20'/%3E%3C/filter%3E%3Crect width='100%25' height='100%25' fill='%23f3f4f6' filter='url(%23b)'/%3E%3C/svg%3E`
}

/**
 * Preload critical images
 */
export function preloadImage(src: string): Promise<void> {
  return new Promise((resolve, reject) => {
    const img = new Image()
    img.onload = () => resolve()
    img.onerror = reject
    img.src = src
  })
}

